''' faça um programa que leia
um número inteiro e mostre na tela o seu sucessor
e seu antecessor'''
numero=int(input("Escolha um número inteiro"))
scss=numero+1
atcss=numero-1
print("o número escolhido foi {}.".format(numero), "\n" \
     "Seu sucessor é {} e seu antecessor é {}".format(scss,atcss))
